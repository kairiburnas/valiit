public class DayOne {
    public static void main(String[] args) {

        System.out.println("Kairi");

        int a = 0;
        int b = 6;

        if (a > 5) {
            System.out.println("Pidurda");
        } else if (a > 3) {
            System.out.println("Pööra");
        } else {
            System.out.println("Hüppa");
        }

        if ((b < 5) && (a > 0)) {
            System.out.println("Lenda");
        } else if ((b < 3) || (a > 0)) {
            System.out.println("Uju");
        } else{
            System.out.println("Seisa");
        }

    }

}


